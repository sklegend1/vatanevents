/* =============================================================================
 *  Step 4 — create posts (skip if title already exists)
 * ===========================================================================*/

function vatan_cseed_create_blog_posts( array $cats, array &$log ): void {
	$catalogue = vatan_cseed_blog_catalogue();
	$created   = 0;
	$skipped   = 0;

	foreach ( $catalogue as $i => $row ) {
		// Idempotency check by title.
		$existing = get_posts( array(
			'post_type'      => 'post',
			'title'          => $row['title'],
			'posts_per_page' => 1,
			'fields'         => 'ids',
			'post_status'    => 'any',
		) );
		if ( $existing ) {
			$skipped++;
			$log[] = '[skip ] post already exists: ' . $row['title'];
			continue;
		}

		$post_id = wp_insert_post( array(
			'post_type'    => 'post',
			'post_status'  => 'publish',
			'post_title'   => $row['title'],
			'post_excerpt' => $row['excerpt'],
			'post_content' => $row['content'],
		), true );

		if ( is_wp_error( $post_id ) ) {
			$log[] = '[fail ] ' . $row['title'] . ': ' . $post_id->get_error_message();
			continue;
		}

		// Featured image (reuse from media seeder pool).
		$att_id = vatan_cseed_image( $row['image'] );
		if ( $att_id ) {
			set_post_thumbnail( $post_id, $att_id );
		}

		// Category.
		if ( ! empty( $cats[ $row['category'] ] ) ) {
			wp_set_post_terms( $post_id, array( (int) $cats[ $row['category'] ] ), 'category', false );
		}

		$created++;
		$log[] = '[ok   ] #' . $post_id . ' "' . $row['title'] . '" (image=' . $att_id . ', cat=' . $row['category'] . ')';
	}

	$log[] = "[done ] {$created} created, {$skipped} skipped.";
}
