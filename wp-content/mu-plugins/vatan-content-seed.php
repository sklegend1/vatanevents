<?php
/**
 * Plugin Name: Vatan Content Seeder
 * Description: Fills the homepage `value_props` block with Persian copy and
 *              creates 10 blog posts with featured images reused from the
 *              media-seeder pool. Visit /wp-admin/?vatan_seed_content=1 as
 *              an administrator to run. Idempotent — re-running skips posts
 *              already created (matched by post_title).
 */

defined( 'ABSPATH' ) || exit;

add_action( 'admin_init', function () {
	if ( empty( $_GET['vatan_seed_content'] ) ) {
		return;
	}
	if ( ! current_user_can( 'manage_options' ) ) {
		wp_die( 'Permission denied.' );
	}

	$log = array();
	vatan_cseed_fill_value_props( $log );
	$cats = vatan_cseed_ensure_blog_categories( $log );
	vatan_cseed_create_blog_posts( $cats, $log );

	wp_die(
		'<h1>Vatan content seed report</h1><pre style="direction:ltr;text-align:left;background:#f6f7f7;padding:16px;border:1px solid #ccd0d4;white-space:pre-wrap">'
		. esc_html( implode( "\n", $log ) )
		. '</pre><p>'
		. '<a href="' . esc_url( admin_url( 'edit.php' ) ) . '">→ View posts</a> &middot; '
		. '<a href="' . esc_url( home_url( '/' ) ) . '">→ Homepage</a>'
		. '</p>',
		'Vatan content seeder',
		array( 'response' => 200 )
	);
} );

/* =============================================================================
 *  Step 1 — fill the value_props block on the homepage layout
 * ===========================================================================*/

function vatan_cseed_fill_value_props( array &$log ): void {
	$layouts = get_option( 'vatan_page_layouts', array() );
	if ( ! is_array( $layouts ) || empty( $layouts['homepage'] ) ) {
		$log[] = '[value_props] no homepage layout — skipping.';
		return;
	}

	$updated = 0;
	foreach ( $layouts['homepage'] as &$block ) {
		if ( ! is_array( $block ) || ( $block['type'] ?? '' ) !== 'value_props' ) {
			continue;
		}
		$block['props'] = array(
			'title'        => 'چرا وطن ایونت؟',
			'subtitle'     => 'هرچه برای کشف، خرید، و برگزاری رویدادهای فارسی‌زبان نیاز دارید — در یک پلتفرم امن و ساده.',
			'item_1_icon'  => '⚡',
			'item_1_title' => 'خرید سریع و امن',
			'item_1_body'  => 'انتخاب صندلی، پرداخت، و دریافت بلیت دیجیتال در کمتر از یک دقیقه — همه چیز رمزنگاری‌شده و گارانتی بازگشت تا ۷۲ ساعت پیش از رویداد.',
			'item_2_icon'  => '📣',
			'item_2_title' => 'اطلاع‌رسانی هوشمند',
			'item_2_body'  => 'به‌محض اعلام تور هنرمند مورد علاقه‌ات در شهر خودت، ایمیل می‌گیری. بلیت‌های مهم را از دست نمی‌دهی.',
			'item_3_icon'  => '🎪',
			'item_3_title' => 'برای برگزارکنندگان',
			'item_3_body'  => 'رویداد خودت را ثبت کن، نقشه‌ی صندلی بساز، و بلیت بفروش. ابزارهای حرفه‌ای فروش و گزارش‌گیری در داشبورد شخصی‌ات.',
		);
		$updated++;
	}
	unset( $block );

	if ( $updated ) {
		update_option( 'vatan_page_layouts', $layouts, true );
		$log[] = "[value_props] updated {$updated} block(s) in homepage layout.";
	} else {
		$log[] = '[value_props] no value_props block found on homepage layout.';
	}
}

/* =============================================================================
 *  Step 2 — ensure blog categories exist
 * ===========================================================================*/

function vatan_cseed_ensure_blog_categories( array &$log ): array {
	$wanted = array(
		'buying-guide'    => 'راهنمای خرید بلیت',
		'organizer-guide' => 'راهنمای برگزارکننده',
		'artists'         => 'هنرمندان',
		'news'            => 'اخبار',
	);

	$resolved = array();
	foreach ( $wanted as $slug => $name ) {
		$t = term_exists( $slug, 'category' );
		if ( ! $t ) {
			$t = wp_insert_term( $name, 'category', array( 'slug' => $slug ) );
		}
		if ( is_wp_error( $t ) ) {
			$log[] = '[cats ] skip ' . $slug . ': ' . $t->get_error_message();
			continue;
		}
		$resolved[ $slug ] = (int) ( is_array( $t ) ? $t['term_id'] : $t );
	}

	$log[] = '[cats ] resolved ' . count( $resolved ) . ' blog category term(s).';
	return $resolved;
}

/* =============================================================================
 *  Helper — look up a previously-seeded attachment by slug
 * ===========================================================================*/

function vatan_cseed_image( string $slug ): int {
	$state = (array) get_option( 'vatan_media_seeded', array() );
	foreach ( $state['attachments'] ?? array() as $key => $id ) {
		if ( str_ends_with( $key, ':' . $slug ) ) {
			return (int) $id;
		}
	}
	$att = get_page_by_path( $slug, OBJECT, 'attachment' );
	return $att ? (int) $att->ID : 0;
}

